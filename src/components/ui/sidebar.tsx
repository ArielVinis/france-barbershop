const SIDEBAR_COOKIE_NAME = "sidebar_state"
const SIDEBAR_COOKIE_MAX_AGE = 60 * 60 * 24 * 7
const SIDEBAR_WIDTH = "16rem"
const SIDEBAR_WIDTH_MOBILE = "18rem"
const SIDEBAR_WIDTH_ICON = "3rem"
const SIDEBAR_KEYBOARD_SHORTCUT = "b"

type SidebarContextProps = {
  state: "expanded" | "collapsed"
  open: boolean
  // eslint-disable-next-line no-unused-vars -- type definition
  setOpen: (value: boolean | ((prev: boolean) => boolean)) => void
  openMobile: boolean
  // eslint-disable-next-line no-unused-vars -- type definition
  setOpenMobile: (value: boolean) => void
  isMobile: boolean
  toggleSidebar: () => void
}

const SidebarContext = React.createContext<SidebarContextProps | null>(null)

function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }
  return context
}

const SidebarProvider = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    defaultOpen?: boolean
    open?: boolean
    // eslint-disable-next-line no-unused-vars -- type definition
    onOpenChange?: (open: boolean) => void
  }
>(
  (
    {
      defaultOpen = true,
      open: openProp,
      onOpenChange: setOpenProp,
      className,
      style,
      children,
      ...props
    },
    ref,
  ) => {
    const isMobile = useIsMobile()
    const [openMobile, setOpenMobile] = React.useState(false)
    const [_open, _setOpen] = React.useState(defaultOpen)
    const open = openProp ?? _open
    const setOpen = React.useCallback(
      // eslint-disable-next-line no-unused-vars -- function type param
      (value: boolean | ((prev: boolean) => boolean)) => {
        const openState = typeof value === "function" ? value(open) : value
        if (setOpenProp) {
          setOpenProp(openState)
        } else {
          _setOpen(openState)
        }
        if (typeof document !== "undefined") {
          document.cookie = `${SIDEBAR_COOKIE_NAME}=${openState}; path=/; max-age=${SIDEBAR_COOKIE_MAX_AGE}`
        }
      },
      [setOpenProp, open],
    )
    const toggleSidebar = React.useCallback(() => {
      return isMobile ? setOpenMobile((p) => !p) : setOpen((p) => !p)
    }, [isMobile, setOpen, setOpenMobile])
    React.useEffect(() => {
      const handleKeyDown = (event: KeyboardEvent) => {
        if (
          event.key === SIDEBAR_KEYBOARD_SHORTCUT &&
          (event.metaKey || event.ctrlKey)
        ) {
          event.preventDefault()
          toggleSidebar()
        }
      }
      window.addEventListener("keydown", handleKeyDown)
      return () => window.removeEventListener("keydown", handleKeyDown)
    }, [toggleSidebar])
    const state: "expanded" | "collapsed" = open ? "expanded" : "collapsed"
    const contextValue = React.useMemo(
      () => ({
        state,
        open,
        setOpen,
        isMobile,
        openMobile,
        setOpenMobile,
        toggleSidebar,
      }),
      [
        state,
        open,
        setOpen,
        isMobile,
        openMobile,
        setOpenMobile,
        toggleSidebar,
      ],
    )
    return (
      <SidebarContext.Provider value={contextValue}>
        <div
          ref={ref}
          className={cn("flex h-full w-full", className)}
          style={{
            ...style,
            ["--sidebar-width" as string]: SIDEBAR_WIDTH,
            ["--sidebar-width-icon" as string]: SIDEBAR_WIDTH_ICON,
            ["--sidebar-width-mobile" as string]: SIDEBAR_WIDTH_MOBILE,
          }}
          data-sidebar="sidebar"
          {...props}
        >
          {children}
        </div>
      </SidebarContext.Provider>
    )
  },
)
SidebarProvider.displayName = "SidebarProvider"

const Sidebar = React.forwardRef<
  HTMLDivElement,
  React.ComponentProps<"div"> & {
    side?: "left" | "right"
    variant?: "sidebar" | "floating" | "inset"
    collapsible?: "offcanvas" | "icon" | "none"
  }
>(
  (
    {
      side = "left",
      variant = "sidebar",
      collapsible = "offcanvas",
      className,
      children,
      ...props
    },
    ref,
  ) => {
    const { isMobile, state, openMobile, setOpenMobile } = useSidebar()

    if (collapsible === "none") {
      return (
        <div
          ref={ref}
          className={cn(
            "flex h-full w-[--sidebar-width] flex-col bg-sidebar text-sidebar-foreground",
            "flex flex-col border-r border-sidebar-border",
            "group/sidebar",
            className,
          )}
          data-state={state}
          data-side={side}
          data-variant={variant}
          data-collapsible={collapsible}
          {...props}
        >
          {children}
        </div>
      )
    }

    if (isMobile) {
      return (
        <Sheet open={openMobile} onOpenChange={setOpenMobile}>
          <SheetContent
            data-side={side}
            className="w-[--sidebar-width-mobile] bg-sidebar p-0 text-sidebar-foreground [&>button]:hidden"
          >
            <SheetTitle className="sr-only">Sidebar</SheetTitle>
            <div className="flex h-full w-full flex-1 flex-col gap-2 overflow-hidden group-data-[collapsible=icon]:w-[--sidebar-width-icon]">
              {children}
            </div>
          </SheetContent>
        </Sheet>
      )
    }

    // Desktop: offcanvas = sidebar escondido quando fechado, vis√≠vel quando aberto
    const isOffcanvas = collapsible === "offcanvas"
    return (
      <div
        ref={ref}
        className={cn(
          "duration-200 ease-linear",
          "group peer hidden md:block",
          isOffcanvas
            ? "w-0 overflow-hidden transition-[width] data-[state=expanded]:w-[--sidebar-width]"
            : "w-[--sidebar-width] data-[state=collapsed]:w-[--sidebar-width-icon]",
        )}
        data-state={state}
        data-side={side}
        data-variant={variant}
        data-collapsible={collapsible}
      >
        <div
          className={cn(
            "flex h-full w-[--sidebar-width] min-w-[--sidebar-width] flex-col bg-sidebar text-sidebar-foreground",
            "border-r border-sidebar-border transition-[width] duration-200 ease-linear",
            !isOffcanvas &&
              "group-data-[state=collapsed]:w-[--sidebar-width-icon] group-data-[state=collapsed]:overflow-hidden",
            className,
          )}
          {...props}
        >
          {children}
        </div>
      </div>
    )
  },
)
Sidebar.displayName = "Sidebar"

const SidebarTrigger = React.forwardRef<
  React.ElementRef<typeof Button>,
  React.ComponentProps<typeof Button>
>(({ className, onClick, ...props }, ref) => {
  const { toggleSidebar } = useSidebar()
  return (
    <Button
      ref={ref}
      data-sidebar="trigger"
      variant="ghost"
      size="icon"
      className={cn(className)}
      onClick={(event) => {
        onClick?.(event)
        toggleSidebar()
      }}
      {...props}
    >
      <PanelLeft />
      <span className="sr-only">Toggle Sidebar</span>
    </Button>
  )
})
SidebarTrigger.displayName = "SidebarTrigger"

